)
GO

CREATE TABLE RECETA_MEDICA_SA (
	CODIGO_RECETA_MEDICA	VARCHAR(255),
	DESCRIP_USO_MEDICO		VARCHAR(255),
	CANTIDAD				VARCHAR(255),
	ID_CLIENTE				VARCHAR(255), 
