)
GO

CREATE TABLE RECETA_X_MEDICAMENTOS_SA (
	CODIGO_RXM				VARCHAR(255),
	ID_MEDICAMENTO			VARCHAR(255),
	CODIGO_RECETA_MEDICA	VARCHAR(255)
)
GO

CREATE TABLE PRECAUCION_SA (
	ID_PRECAUCION		VARCHAR(255),
	EFECTOS_SECUNDARIOS	VARCHAR(255),
